#include "../libft.h"

// searches for occurence of "needle" in a "haystack"
// if needle is empty return haystack
// iterate through the haystack until end or len
// inner loop checks for match in haystack that corresponds to needle
// if end if needle is reached, it means a full match has been found
// --> return pointer to beginning of match in haystack

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	size_t	i;
	size_t	j;

	i = 0;
	if (needle[i] == '\0')
		return ((char *)haystack);
	while (haystack[i] != '\0' && i < len)
	{
		j = 0;
		while (haystack[i + j] == needle[j] && i + j < len)
		{
			if (needle[j + 1] == '\0')
				return ((char *)(haystack + i));
			j++;
		}
		i++;
	}
	return (NULL);
}
