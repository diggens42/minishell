#include "../libft.h"

// copy strings safely by preventing buffer overflows
// copies up to size - 1 chars from 'src' to 'dst'
// then adds terminating null char

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	src_len;
	size_t	i;

	src_len = ft_strlen(src);
	if (size == 0 || dest == NULL)
		return (src_len);
	i = 0;
	while (i < size - 1 && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (src_len);
}
