#include "../libft.h"

// 1. compares two blocks of memory for the first 'n' bytes
// 2. returns diff betweens first pair of bytes that are different
// 3. returns 0 if they are identical in the range of 'n'

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const unsigned char	*str1;
	const unsigned char	*str2;
	size_t				i;

	str1 = s1;
	str2 = s2;
	i = 0;
	while (i < n)
	{
		if (str1[i] != str2[i])
			return (str1[i] - str2[i]);
		i++;
	}
	return (0);
}
